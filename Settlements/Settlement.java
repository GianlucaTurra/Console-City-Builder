package Settlements;

abstract class Settlement {

    // ----------------------------------------------------------------------------------------------------------------
    // CONSTRUCTOR
    // ----------------------------------------------------------------------------------------------------------------
    protected String name;
    public Settlement(String name) {
        this.name = name;
    }

    // ----------------------------------------------------------------------------------------------------------------
    // Encapsulation for name
    // ----------------------------------------------------------------------------------------------------------------
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
}
